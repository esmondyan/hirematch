def _is_jd_section_header(line: str) -> bool:
    """Check if a line is a JD section header."""
    stripped = line.strip()
    if not stripped or len(stripped) > 30:
        return False
    for pattern in _JD_SECTION_PATTERNS:
        if re.match(pattern, stripped):
            return True
    return False


def jd_to_html(text: str) -> str:
    """Convert JD text to formatted HTML for display.

    Unlike resume_to_html, this does NOT clean noise (JD is user-written).
    It only adds structural formatting: section headers, lists, paragraphs.
    """
    if not text:
        return '<p class="empty-text">ж— еҶ…е®№</p>'

    lines = text.strip().split('\n')
    html_parts = []
    in_list = False
    in_para = False

    i = 0
    while i < len(lines):
        line = lines[i].strip()

        if not line:
            if in_list:
                html_parts.append('</ul>')
                in_list = False
            if in_para:
                html_parts.append('</p>')
                in_para = False
            i += 1
            continue

        # JD section header detection
        if _is_jd_section_header(line):
            if in_list:
                html_parts.append('</ul>')
                in_list = False
            if in_para:
                html_parts.append('</p>')
                in_para = False
            html_parts.append(f'<h3 class="resume-section jd-section">{_escape_html(line)}</h3>')
            i += 1
            continue

        # Also check resume section patterns (may overlap with JD)
        if _is_section_header(line):
            if in_list:
                html_parts.append('</ul>')
                in_list = False
            if in_para:
                html_parts.append('</p>')
                in_para = False
            html_parts.append(f'<h3 class="resume-section jd-section">{_escape_html(line)}</h3>')
            i += 1
            continue

        # List item detection
        list_match = re.match(r'^[\-\вҖў\*\в–Ә\в–ё\вҡ«\вҰҒ\вҡ¬\вң“\вң”\вҳ‘]\s+(.+)', line)
        if list_match:
            if not in_list:
                html_parts.append('<ul class="resume-list jd-list">')
                in_list = True
            html_parts.append(f'<li>{_format_inline(_escape_html(list_match.group(1)))}</li>')
            i += 1
            continue

        # Numbered list: "1. xxx", "1гҖҒxxx", "(1) xxx"
        num_match = re.match(r'^(\d+[\.\)гҖҒ])\s*(.+)', line)
        if num_match:
            if not in_list:
                html_parts.append('<ul class="resume-list jd-list">')
                in_list = True
            html_parts.append(f'<li>{_format_inline(_escape_html(num_match.group(2)))}</li>')
            i += 1
            continue

