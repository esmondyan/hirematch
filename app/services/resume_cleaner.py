def _is_date_start(line: str) -> bool:
    """Check if line starts with a date range (common resume pattern).

    Matches:
      - 2023.03-иҮід»Ҡ / 2023е№ҙ3жңҲ-иҮід»Ҡ / 2023/03-зҺ°еңЁ
      - 2020.01-2023.06 / 2020е№ҙ1жңҲ-2023е№ҙ6жңҲ
    """
    return bool(re.match(
        r'^\d{4}[\.\-\/е№ҙ]\d{1,2}[жңҲ]?\s*[\-\вҖ“вҖ”иҮіеҲ°]\s*(\d{4}[\.\-\/е№ҙ]\d{1,2}[жңҲ]?|иҮід»Ҡ|зҺ°еңЁ)',
        line,
    ))


def _merge_broken_paragraphs(lines: list[str]) -> list[str]:
    """Merge lines broken by PDF extraction, but preserve intentional structure.

    Merging rules (ALL must be true to merge):
    1. Current line doesn't end with sentence-ending punctuation
    2. Next line exists and is not empty
    3. Neither line is a header, list item, date start, or sub-item start
    4. Current line is short (< 30 chars) вҖ” long lines stand alone
    5. Current merged buffer hasn't exceeded reasonable paragraph size
    """
    if not lines:
        return lines

    result = []
    buffer = ''

    for i, line in enumerate(lines):
        stripped = line.strip()

        # Empty line: flush buffer
        if not stripped:
            if buffer:
                result.append(buffer)
                buffer = ''
            result.append('')
            continue

        # Never merge these: headers, list items, date starts, sub-item starts
        is_break = (
            _is_section_header(stripped) or
            _is_list_item(stripped) or
            _is_date_start(stripped) or
            _is_sub_item_start(stripped)
        )

        if is_break:
            if buffer:
                result.append(buffer)
                buffer = ''
            result.append(stripped)
            continue

        # Is there a next line to potentially merge with?
        has_next = i + 1 < len(lines)
        next_line = lines[i + 1].strip() if has_next else ''
        next_is_break = (
            _is_section_header(next_line) or
            _is_list_item(next_line) or
            _is_date_start(next_line) or
            _is_sub_item_start(next_line)
        ) if has_next else True

        # Decide whether to merge
        should_merge = (
            has_next and
            next_line and  # Next line not empty
            not _is_sentence_end(stripped) and  # Doesn't end sentence
            not next_is_break and  # Next isn't a structural break
            len(stripped) < 30 and  # Current line is short (likely broken)
            len(buffer + stripped) < 120  # Don't make paragraphs too long
        )

        if buffer:
            if should_merge:
                buffer += stripped
            else:
                buffer += stripped
                result.append(buffer)
                buffer = ''
        else:
            if should_merge:
                buffer = stripped
            else:
                result.append(stripped)

    if buffer:
        result.append(buffer)

    return result


def _remove_hidden_data_fragments(lines: list[str]) -> list[str]:
    """Remove isolated short fragments that are likely PDF hidden/embedded data.

    Some PDF providers embed DRM watermarks or tracking data as hidden text.
    When extracted, these appear as short isolated lines that don't connect
    to surrounding resume content.

    Also removes dangling section-number-like fragments (e.g. "3.", "IV.")
    that aren't part of real content.
    """
    if len(lines) < 3:
        return lines

    result = []
    first_content_line = True  # Protect first non-empty line (often the name)
    for i, line in enumerate(lines):
        stripped = line.strip()

        # Keep empty lines
        if not stripped:
            result.append(line)
            continue

        # Never remove lines containing email addresses
        if _contains_email(stripped):
            first_content_line = False
            result.append(line)
            continue

        # Keep recognized section headers
        if _is_section_header(stripped):
            first_content_line = False
            result.append(line)
            continue

        # Keep list items
        if _is_list_item(stripped):
            first_content_line = False
            result.append(line)
            continue

        # Check if this is an isolated short fragment
        t = len(stripped)
        if t <= 6 and not first_content_line:
            prev_empty = i == 0 or not lines[i - 1].strip()
            next_empty = i == len(lines) - 1 or not lines[i + 1].strip()

            # Isolated between empty lines вҶ’ likely hidden data fragment
            if prev_empty and next_empty:
                continue

            # Isolated before/after a section header вҶ’ likely artifact
            if prev_empty and i + 1 < len(lines) and _is_section_header(lines[i + 1].strip()):
                continue
            if next_empty and i > 0 and _is_section_header(lines[i - 1].strip()):
                continue

        # Remove lines that look like dangling section numbers
        # e.g. "3.", "IV.", "(2)", "дёүгҖҒ"
        # Never remove the first content line (likely a name)
        if re.match(r'^[\dIVXivx]+[\.\)гҖҒпјү]?$', stripped) and t <= 5 and not first_content_line:
            prev_empty = i == 0 or not lines[i - 1].strip()
            next_empty = i == len(lines) - 1 or not lines[i + 1].strip()
            if prev_empty and next_empty:
                continue

        first_content_line = False
        result.append(line)

    return result


def _is_markdown(text: str) -> bool:
    """Detect if text is markdown by looking for structural markers."""
    lines = text.split('\n')
    md_lines = 0
    for line in lines[:50]:  # Sample first 50 lines
        stripped = line.strip()
        if not stripped:
            continue
        if re.match(r'^#{1,6}\s', stripped):  # Headers
            md_lines += 1
        elif re.match(r'^[\-\*\+]\s', stripped):  # Unordered lists
            md_lines += 1
        elif re.match(r'^\*\*.*\*\*', stripped):  # Bold
            md_lines += 1
        elif re.match(r'^\|.+\|', stripped):  # Tables
            md_lines += 1
    return md_lines >= 2


def _clean_markdown(text: str) -> str:
    """Light cleaning for markdown: remove garbled lines and noise, preserve structure."""
    if not text:
        return ""

    lines = text.split('\n')
    cleaned = []

    for line in lines:
        stripped = line.strip()

        # Preserve empty lines (markdown uses them for paragraph breaks)
        if not stripped:
            cleaned.append('')
            continue

        # Remove whole-line hash/ID strings (PDF metadata)
        if _is_hash_or_id_string(stripped):
            continue

        # Remove image/object placeholders from pymupdf4llm
        if _IMAGE_PLACEHOLDER_RE.search(stripped):
            continue

        # Remove lines that are just a markdown image ref
        if _MD_IMAGE_REF_RE.match(stripped):
            continue

        # Remove garbled lines
        if _is_garbled_line(stripped):
            continue

        # Light inline noise cleaning (but preserve markdown syntax)
        cleaned_line = _clean_line_noise_markdown(stripped)
        if cleaned_line:
            cleaned.append(cleaned_line)

    # Collapse multiple consecutive empty lines
    collapsed = []
    prev_empty = False
    for line in cleaned:
        if line == '':
            if not prev_empty:
                collapsed.append(line)
                prev_empty = True
        else:
            collapsed.append(line)
            prev_empty = False

    return '\n'.join(collapsed).strip()


def _clean_line_noise_markdown(line: str) -> str:
    """Clean inline noise from a markdown line, preserving markdown syntax.

    Less aggressive than _clean_line_noise вҖ” only removes things that are
    definitely garbage, never touches markdown formatting.
    """
    # Protect email addresses
    emails = _EMAIL_RE.findall(line)
    for i, email in enumerate(emails):
        line = line.replace(email, f'__EMAIL_{i}__')

    # Remove mojibake sequences
    line = re.sub(r'[\xc2-\xf4][\x80-\xbf]{1,2}', '', line)

    # Remove UUID patterns
    line = re.sub(r'\b[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{12}\b', '', line)

    # Remove long hash tokens (>30 chars, mixed case/digits/hyphens)
    line = re.sub(r'\b[a-zA-Z0-9_-]{30,}\b', '', line)

    # Remove inline image placeholders and markdown image refs
    line = _IMAGE_PLACEHOLDER_RE.sub('', line)
    line = _MD_IMAGE_REF_RE.sub('', line)

    # Collapse whitespace (but not newlines)
    line = re.sub(r'[^\S\n]+', ' ', line)

    # Restore emails
    for i, email in enumerate(emails):
        line = line.replace(f'__EMAIL_{i}__', email)

    return line.strip()


def clean_resume_text(text: str) -> str:
    """Clean resume text: remove garbled lines, inline noise.

    Routes to appropriate cleaner based on whether input is markdown or plain text.
    """
    if not text:
        return ""

    if _is_markdown(text):
        return _clean_markdown(text)

    # === Plain text path (original logic) ===
    lines = text.split('\n')
    cleaned_lines = []

    for line in lines:
        stripped = line.strip()

        if not stripped:
            cleaned_lines.append('')
            continue

        # Remove whole-line hash/ID strings
        if _is_hash_or_id_string(stripped):
            continue

        if _is_garbled_line(stripped):
            continue

        cleaned = _clean_line_noise(stripped)
        if not cleaned:
            continue
        cleaned_lines.append(cleaned)

    # Collapse multiple consecutive empty lines into at most one
    collapsed = []
    prev_empty = False
    for line in cleaned_lines:
        if line == '':
            if not prev_empty:
                collapsed.append(line)
                prev_empty = True
        else:
            collapsed.append(line)
            prev_empty = False

    # Remove isolated fragments from PDF hidden data
    cleaned = _remove_hidden_data_fragments(collapsed)

    return '\n'.join(cleaned).strip()


def _markdown_to_html(md_text: str) -> str:
    """Convert markdown to styled HTML for resume display.

    Uses python-markdown with extensions for tables and fenced code,
    then wraps in resume-specific CSS classes.
    """
    html = _md_lib.markdown(
        md_text,
        extensions=['tables', 'fenced_code'],
    )
    # Wrap in a container div for resume-specific styling
    return f'<div class="resume-md">{html}</div>'


def resume_to_html(text: str) -> str:
    """Convert resume text to HTML for display.

    Uses markdownвҶ’HTML conversion for markdown input,
    custom heuristics for plain text.
    """
    if not text:
        return '<p class="empty-text">ж— еҶ…е®№</p>'

    cleaned = clean_resume_text(text)
    if not cleaned:
        return '<p class="empty-text">ж— еҶ…е®№</p>'

    if _is_markdown(cleaned):
        return _markdown_to_html(cleaned)

    lines = cleaned.split('\n')
    html_parts = []
    in_list = False
    in_para = False

    i = 0
    while i < len(lines):
        line = lines[i].strip()

        if not line:
            if in_list:
                html_parts.append('</ul>')
                in_list = False
            if in_para:
                html_parts.append('</p>')
                in_para = False
            i += 1
            continue

        # Section header detection
        if _is_section_header(line):
            if in_list:
                html_parts.append('</ul>')
                in_list = False
            if in_para:
                html_parts.append('</p>')
                in_para = False
            html_parts.append(f'<h3 class="resume-section">{_escape_html(line)}</h3>')
            i += 1
            continue

        # List item detection
        list_match = re.match(r'^[\-\вҖў\*\в–Ә\в–ё\вҡ«\вҰҒ\вҡ¬\вң“\вң”\вҳ‘]\s+(.+)', line)
        if list_match:
            if not in_list:
                html_parts.append('<ul class="resume-list">')
                in_list = True
            html_parts.append(f'<li>{_format_inline(_escape_html(list_match.group(1)))}</li>')
            i += 1
            continue

        # Numbered list
        num_match = re.match(r'^(\d+[\.\)гҖҒ])\s*(.+)', line)
        if num_match:
            if not in_list:
                html_parts.append('<ul class="resume-list">')
                in_list = True
            html_parts.append(f'<li>{_format_inline(_escape_html(num_match.group(2)))}</li>')
            i += 1
            continue

        # Date range line (e.g. "2023.03-иҮід»Ҡ", "2020.01-2023.06")
        date_match = re.match(
            r'^(\d{4}[\.\-\/е№ҙ]\d{1,2}[жңҲ]?\s*[\-\вҖ“вҖ”иҮіеҲ°]\s*(\d{4}[\.\-\/е№ҙ]\d{1,2}[жңҲ]?|иҮід»Ҡ|зҺ°еңЁ))(.*)',
            line,
        )
        if date_match:
            if in_list:
                html_parts.append('</ul>')
                in_list = False
            if not in_para:
                html_parts.append('<p class="resume-para">')
                in_para = True
            formatted = (
                f'<span class="resume-date">{_escape_html(date_match.group(1))}</span>'
                f'{_format_inline(_escape_html(date_match.group(2)))}'
            )
            html_parts.append(formatted)
            i += 1
            continue

        # Regular paragraph
        if in_list:
            html_parts.append('</ul>')
            in_list = False
        if not in_para:
            html_parts.append('<p class="resume-para">')
            in_para = True
        else:
            html_parts.append('<br>')
        html_parts.append(_format_inline(_escape_html(line)))
        i += 1

    if in_list:
        html_parts.append('</ul>')
    if in_para:
        html_parts.append('</p>')

    return '\n'.join(html_parts)


