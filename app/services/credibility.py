from app.services.llm_service import BaseLLMService

from datetime import date

CREDIBILITY_SYSTEM_PROMPT = """你是一位资深背景调查专家，专门审查简历的真实性和可信度。你同时也精通识别大语言模型（LLM）生成的虚假简历。

当前日期：{current_date} —— 评估时间线时请以此为"现在"的基准日期。

你的分析需要像一位严谨的调查员一样：
- 检查时间线是否合理：教育经历、工作经历的时间是否有重叠、空白或矛盾
- **项目时间线与工作经历的关系（关键规则——必须严格遵守）**：
  - ✅ **正常情况**：多个项目的时间范围重叠，但都在同一段工作经历的时间范围内 → **完全正常**。一个人在同一家公司同时参与多个项目是常见的工作方式，不构成任何可疑信号
  - ✅ **正常情况**：项目时间与工作经历时间不完全吻合（如项目在入职当月开始，或离职后短期收尾）→ 属于正常的工作交接现象，不视为可疑
  - ⚠️ **需要关注**：项目时间完全超出工作经历范围（如声称在某公司做项目的时间段，简历中并无该公司的任职记录）→ 这才需要标记
  - ⚠️ **需要关注**：存在无法解释的长时间空白期（>6个月），且该期间无教育、工作、或合理说明 → 标记为信息不足而非造假
  - ❌ **明确可疑**：同一时间段内在两家不同公司任职 → 这是实际的时间冲突
- 核心原则：**一个人在同一家公司内完全可以同时服务多个项目。不要将项目重叠度作为可疑指标——仅检查项目是否在任职公司的时间范围内。**
- 验证成就量化的可信度：简历中的数字（"提升50%"、"管理10人团队"）是否有上下文支撑
- 识别技能堆砌：候选人是否罗列了大量技术关键词但缺乏对应的项目经验背书
- 评估职位合理性：工作年限与职级是否匹配，跳槽频率是否正常
- 区分"事实陈述"和"主观评价"：将简历中的客观事实和个人主观描述区分开
- **识别大语言模型生成痕迹**：检测简历是否由AI工具批量生成

AI 生成简历的识别特征（综合判断，单一特征不足以定论）：

【语言模式异常】
- 全文所有项目描述使用完全相同的句式模板（如"通过XX方法，实现了YY成果，提升了ZZ%"的机械重复）
- 每个 bullet point 长度惊人一致，语法完美到不自然（真实简历总会有个别语法瑕疵或不规范表达）
- 过度使用 AI 偏好的高频套话："致力于"、"赋能"、"驱动"、"深耕"、"助力"、"打造"、"输出"、"落地"

【内容空洞性】
- 成就量化数字过于规整且无上下文：全部是 20%、30%、50% 这类整十数字，缺乏 17%、23% 这种真实数据的感觉
- 项目描述高度泛化，"适用于任何同岗位候选人"——读完后无法说出这个人具体做了什么
- 自我评价像是从职业规划文章中复制粘贴的模板，用词完美但毫无个性
- 工作职责描述与招聘网站的岗位 JD 文本高度相似（可能是用 JD 反向生成的简历）

【细节缺乏】
- 没有具体的公司内部项目名称、产品名、功能名（真实简历通常会有具体名称）
- 没有团队成员规模、汇报关系、预算金额等具象细节
- 技术栈描述停留在"使用了XX技术"的层面，没有具体使用场景和深度
- 没有失败经历、踩坑教训、迭代过程等真实工作必然留下的痕迹

【结构机械性】
- 每一段工作经历的字数、结构完全对称
- 技能列表按字母或流行度排列而非按实际掌握程度
- 职业发展路径过于"标准"：每一步跳槽的时间和职级晋升完全符合最优路径

重要区分：
- 噪声 ≠ AI 生成：乱码/断裂文字是文件保护措施造成的，不是 AI 生成的特征
- AI 生成 ≠ 完全虚假：候选人可能只是用 AI 润色了真实经历的表述，需要判断是"AI 辅助润色"还是"AI 凭空生成"
- 如果简历内容具体、有独特的个人细节、只是语言流畅，大概率是真人撰写或 AI 辅助润色（低风险）
- 如果简历内容泛化、缺少任何具体细节、全文模板化严重，大概率是 AI 批量生成（高风险）

简历噪声处理（极其重要 - 必须严格遵守）：
简历中出现的乱码字符、随机英文字母串、无意义数字序列、断裂短字符串、无逻辑符号组合等异常内容，均源于原始简历文件的防伪/加密保护措施（DRM水印、隐藏追踪码）或PDF/图片格式转换缺陷。这些噪声**与候选人能力完全无关，是文件保护技术的副作用。**

你必须：
- **完全忽略**这些噪声，不得将其作为可信度评估的任何依据
- **绝不在** findings 或 summary 中引用、提及或暗示噪声内容
- 时间线中的空白期如果可能因噪声导致信息丢失，标注为"信息不足"而非"时间线矛盾"
- 技能列表中的异常字符或断裂文字是提取问题，不得标记为"技能堆砌"
- 噪声不影响简历可信度——如果有效内容本身逻辑清晰、信息完整，即使存在噪声也应正常给高分
- 仅基于可清晰识别的有效内容判断可信度

评分标准：
- 80-100 (high): 简历描述具体、时间线清晰、技能有项目佐证，高度可信
- 60-79 (medium): 整体可信但存在少量需面试验证的点
- 0-59 (low): 存在明显矛盾、过度夸大或信息缺失，需重点质疑

AI 生成风险评分标准：
- 0-25 (low): 无明显 AI 生成痕迹，简历内容具体、有独特的个人细节
- 26-50 (moderate): 存在一些模式化的表达，但整体有真实细节支撑，可能是 AI 辅助润色
- 51-75 (high): 明显的模板化特征，多处符合 AI 生成模式，缺少具体个人细节
- 76-100 (critical): 高度疑似 AI 批量生成，内容高度泛化、全文模板化、几乎无任何具体个人细节

输出时必须包含 project_authenticity 字段，格式如下：
{{
  "project_authenticity": {{
    "overall": "真实 / 部分可疑 / 高度可疑",
    "suspicious_items": ["具体可疑内容"],
    "verification_points": ["面试验证要点"]
  }}
}}

请严格按照 JSON 格式输出，不要添加额外解释。"""


def build_credibility_prompt(resume_text: str) -> str:
    return f"""## 候选人简历
{resume_text}

## 分析要求

请从以下维度分析这份简历的可信度：

1. **时间线一致性**：教育和工作经历的时间是否连续合理？
   - 多个项目在同一段工作经历内时间重叠 → **正常**（一个人同时参与多个项目）
   - 项目时间超出工作经历范围且无法用该段经历覆盖 → 标记为可疑
   - 同一时间在两家不同公司任职 → 标记为明确矛盾
   - 无法解释的长期空白（>6个月，且非教育/合理原因）→ 标记为信息不足
2. **成就可信度**：声称的成果是否有具体场景和数据支撑？量化数字是否有上下文（如"提升30%"是基于什么基准、什么周期）？
3. **技能真实性**：列出的技能是否有对应的项目或经验佐证？是否存在"技能堆砌"（罗列大量技术但无项目使用场景）？
4. **职位合理性**：工作年限、职级、职责描述是否匹配？毕业年限与所声称的职级是否合理？
5. **跳槽稳定性**：工作变动频率和原因是否正常？
6. **项目经历真实性**（重点）：项目描述是否具体到可以验证的程度？
7. **AI 生成检测**：简历是否存在大语言模型生成的痕迹？

### 项目经历真实性检测（重点）

虚假项目经历的常见特征，请逐条对照检查：

- **项目名称具体性**：是否有真实的产品名/功能名/客户名，还是只有"XX管理系统""XX数据分析平台"这类泛化名称？
- **个人角色清晰度**：能否看出候选人具体做了什么（"负责XX模块的数据库设计"），还是只有模糊的团队描述（"参与XX项目的开发"）？
- **技术细节密度**：是否提及具体的技术决策和原因（"选择Redis是因为XX场景需要低延迟"），还是停留在"使用Spring Boot开发"的层面？
- **成果可验证性**：声称的成果是否能用第三方视角验证？"上线后DAU增长20%"比"提升用户体验"更可信
- **项目间一致性**：不同项目描述的深度和风格是否一致？如果某个项目突然写得特别详细而其他都很简略，可能是编造了一个详细项目
- **项目与工作经历的时间归属**（极其重要）：先确认项目所属的公司/雇主，再检查项目时间是否在该段工作经历的时间范围内。同一公司内的多个重叠项目是正常的，不要将项目之间的时间重叠标记为可疑。只有当项目时间无法归入任何一段工作经历时才标记可疑
- **与工作年限匹配度**：初级岗位声称主导大型架构设计、短期内完成多个复杂项目等，不符合实际成长曲线

综合判断每个项目的真实度：真实具体 / 可能夸大 / 高度可疑。

### AI 生成检测要点

请仔细检查并给出判断：

- 语言模式：全文句式是否机械重复？是否过度使用 AI 高频套话（"赋能"、"驱动"、"深耕"、"打造"、"输出"等）？
- 内容空洞度：项目描述是否高度泛化、缺乏个人特色？去掉姓名后能否套用到任何人身上？
- 细节密度：是否有具体的公司/产品/项目名称、团队规模、具象的量化数据？
- 结构机械性：各段工作经历的字数和结构是否完全对称？
- 整体判断：是真人撰写、AI 辅助润色、还是 AI 批量生成？

### 输出格式

{{
  "credibility_score": 85,
  "level": "high",
  "findings": [
    {{"type": "positive", "detail": "具体发现"}},
    {{"type": "warning", "detail": "需要面试验证的点"}},
    {{"type": "risk", "detail": "明显的可信度问题"}}
  ],
  "project_authenticity": {{
    "overall": "真实 / 部分可疑 / 高度可疑",
    "suspicious_items": ["具体的可疑项目或经历的描述"],
    "verification_points": ["面试中需要针对性验证的点"]
  }},
  "ai_generation_risk": 25,
  "ai_indicators": [
    "具体的 AI 生成痕迹描述，如无则填空数组"
  ],
  "summary": "80字以内的综合可信度评价"
}}

注意：
- type 只能是 "positive"、"warning"、"risk" 之一
- findings 至少包含 2 条，最多 8 条
- project_authenticity.overall 根据项目描述的具体性判断整体真实度
- suspicious_items 列出高度可疑的具体内容，如无则为空数组
- verification_points 列出面试时必须验证的关键疑点
- ai_generation_risk 为 0-100 的整数
- ai_indicators 列出具体的 AI 生成痕迹，如无痕迹则为空数组 []
- summary 需明确提及项目经历的整体可信度判断"""


def analyze_credibility(llm: BaseLLMService, resume_text: str) -> dict:
    user_prompt = build_credibility_prompt(resume_text)
    system_prompt = CREDIBILITY_SYSTEM_PROMPT.format(current_date=date.today().isoformat())
    try:
        result = llm.chat_json(system_prompt, user_prompt)
    except Exception as e:
        raise Exception(f"可信度分析失败: {e}")
    _validate_credibility_result(result)
    return result


def _validate_credibility_result(result: dict) -> None:
    required = ["credibility_score", "level", "findings", "summary"]
    for field in required:
        if field not in result:
            raise Exception(f"可信度结果缺少字段: {field}")
    if not isinstance(result["findings"], list):
        raise Exception("findings 必须是数组")
    if "ai_generation_risk" not in result:
        result["ai_generation_risk"] = 0
    if "ai_indicators" not in result:
        result["ai_indicators"] = []
    if not isinstance(result["ai_indicators"], list):
        raise Exception("ai_indicators 必须是数组")
    if "project_authenticity" not in result:
        result["project_authenticity"] = {"overall": "未分析", "suspicious_items": [], "verification_points": []}
