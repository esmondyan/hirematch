def restructure_resume_markdown(llm, raw_markdown: str) -> str:
    """Use LLM to reorganize resume markdown into clean, well-structured markdown.

    Args:
        llm: An LLM service instance (must have a ``chat`` method).
        raw_markdown: The raw/cleaned markdown text from PDF parsing.

    Returns:
        Well-structured markdown text, or the original text on failure.
    """
    if not raw_markdown or not raw_markdown.strip():
        return raw_markdown

    # Skip if text is too short to benefit from restructuring
    if len(raw_markdown) < 100:
        return raw_markdown

    user_prompt = f"иҜ·ж•ҙзҗҶд»ҘдёӢз®ҖеҺҶеҶ…е®№пјҡ\n\n{raw_markdown}"

    try:
        result = llm.chat(_RESTRUCTURE_SYSTEM_PROMPT, user_prompt, max_tokens=4096, temperature=0.1)
        if result and len(result.strip()) > 50:
            return result.strip()
        return raw_markdown
    except Exception:
        return raw_markdown
